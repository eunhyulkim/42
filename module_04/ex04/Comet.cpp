/* ------------------------------- DESTRUCTOR ------------------------------- */
/* ************************************************************************** */

Comet::~Comet() {}

/* ************************************************************************** */
/* -------------------------------- OVERLOAD -------------------------------- */
/* ************************************************************************** */

Comet& Comet::operator=(const Comet& obj)
{
	if (this == &obj)
		return (*this);
	this->m_name = obj.get_m_name();
	return (*this);
}

std::string
Comet::beMined(DeepCoreMiner *) const {
	return ("Meium");
}

std::string
Comet::beMined(StripMiner *) const {
	return ("Tartarite");
}

std::string
Comet::get_m_name() const {
	return (this->m_name);
}

