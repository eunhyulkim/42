#include "libft.h"

int		ft_iszero(char *str)
{
	if (!str || !(*str))
		return (0);
	if (*str == '+' || *str == '-' || *str == ' ')
		str++;
	while (*str == '0')
		str++;
	if (*str != 0)
		return (0);
	return (1);
}
